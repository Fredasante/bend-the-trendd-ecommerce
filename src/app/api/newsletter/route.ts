import { NextResponse } from "next/server";
import { Resend } from "resend";

const resend = new Resend(process.env.RESEND_API_KEY);

export async function POST(req: Request) {
  try {
    const { firstName, email, interest } = await req.json();

    // Send welcome email using Resend
    const { data, error } = await resend.emails.send({
      from: "Bend The Trendd <onboarding@bendthetrendd.com>",
      to: [email],
      subject: `Welcome to Bend The Trendd, ${firstName}!`,
      html: `
        <!DOCTYPE html>
        <html>
          <head>
            <meta charset="utf-8">
            <meta name="viewport" content="width=device-width, initial-scale=1.0">
          </head>
          <body style="margin: 0; padding: 0; font-family: -apple-system, BlinkMacSystemFont, 'Segoe UI', Roboto, 'Helvetica Neue', Arial, sans-serif;">
            <table role="presentation" style="width: 100%; border-collapse: collapse;">
              <tr>
                <td align="center" style="padding: 40px 0;">
                  <table role="presentation" style="width: 600px; max-width: 100%; border-collapse: collapse; background-color: #ffffff;">
                    <!-- Header -->
                    <tr>
                      <td style="padding: 40px 30px; text-align: center; background-color: #382423;">
                        <h1 style="margin: 0; color: #ffffff; font-size: 28px; font-weight: bold;">
                          Welcome, ${firstName}!
                        </h1>
                      </td>
                    </tr>
                    
                    <!-- Body -->
                    <tr>
                      <td style="padding: 40px 30px;">
                        <p style="margin: 0 0 20px; color: #333333; font-size: 16px; line-height: 1.6;">
                          Thank you for joining Bend The Trendd! We're excited to have you in our community.
                        </p>
                        <p style="margin: 0 0 20px; color: #333333; font-size: 16px; line-height: 1.6;">
                          You'll receive updates about new arrivals, exclusive offers, and style inspiration curated just for you.
                        </p>
                        ${
                          interest
                            ? `
                        <p style="margin: 0 0 20px; color: #333333; font-size: 16px; line-height: 1.6;">
                          We noticed you're interested in <strong>${interest}</strong> — we'll make sure to send you updates on pieces you'll love!
                        </p>
                        `
                            : ""
                        }
                        <div style="text-align: center; margin: 30px 0;">
                          <a href="https://www.bendthetrendd.com/shop" style="display: inline-block; padding: 14px 32px; background-color: #382423; color: #ffffff; text-decoration: none; border-radius: 8px; font-weight: 600; font-size: 16px;">
                            Start Shopping
                          </a>
                        </div>
                      </td>
                    </tr>
                    
                    <!-- Footer -->
                    <tr>
                      <td style="padding: 30px; background-color: #f8f8f8; text-align: center; border-top: 1px solid #eeeeee;">
                        <p style="margin: 0 0 10px; color: #666666; font-size: 14px;">
                          Stay connected with us
                        </p>
                        <p style="margin: 0 0 15px; color: #666666; font-size: 14px;">
                          <a href="#" style="color: #382423; text-decoration: none; margin: 0 10px;">Instagram</a> |
                          <a href="#" style="color: #382423; text-decoration: none; margin: 0 10px;">Facebook</a> |
                          <a href="#" style="color: #382423; text-decoration: none; margin: 0 10px;">Pinterest</a>
                        </p>
                        <p style="margin: 15px 0 0; color: #999999; font-size: 12px;">
                          You're receiving this email because you subscribed to our newsletter.<br>
                          <a href="{{unsubscribe_url}}" style="color: #999999; text-decoration: underline;">Unsubscribe</a>
                        </p>
                      </td>
                    </tr>
                  </table>
                </td>
              </tr>
            </table>
          </body>
        </html>
      `,
      // Plain text version for better deliverability
      text: `
Welcome, ${firstName}!

Hey Gorgeous!

Thank you for subscribing to our newsletter! We're thrilled to have you join our community of women who set the tone, not the trend.

You'll be the first to know about:
- New arrivals & exclusive collections
- Special discounts & early access
- Style tips & fashion inspiration
- VIP-only offers

${interest ? `We noticed you're interested in ${interest} — we'll make sure to send you updates on pieces you'll love!\n\n` : ""}

Visit us: https://www.bendthetrendd.com/shop

Stay connected with us on Instagram, Facebook, and Pinterest.

You're receiving this email because you subscribed to our newsletter.
      `.trim(),
    });

    if (error) {
      console.error("Resend error:", error);
      return NextResponse.json(
        { success: false, error: error.message },
        { status: 500 }
      );
    }

    console.log("Email sent successfully:", data);
    return NextResponse.json({ success: true, emailId: data?.id });
  } catch (error: any) {
    console.error("Error:", error);
    return NextResponse.json(
      { success: false, error: error.message },
      { status: 500 }
    );
  }
}
